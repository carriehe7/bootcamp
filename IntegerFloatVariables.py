# define simple variables for further manipulations
simple_variable_a = 5
simple_variable_b = 10

# summary of 2 variables
sum_result = simple_variable_a + simple_variable_b
print('sum result : ' +str(sum_result))

# multiply 2 numbers
multiply_result = simple_variable_a * simple_variable_b
print('multuply result : ' +str(multiply_result))

# divide 2 numbers
divide_result = simple_variable_b / simple_variable_a
print('divide result : ' +str(divide_result))

# power 2 numbers
power_result = simple_variable_a ** 2
print('power result : ' +str(power_result))

# module action on 2 numbers
module_result = simple_variable_a % 2
print('module result : ' +str(module_result))


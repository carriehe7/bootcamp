print('casting from int to str')
int_var = 5
int_var_casted_to_str = str(int_var)
print(type(int_var_casted_to_str))
print(int_var)

print('*****')
print('casting from int to float')
int_var_casted_to_float = float(int_var)
print(type(int_var_casted_to_float))
print(int_var_casted_to_float)

print('*****')
print('casting from float to int')
float_var_casted_to_int = int(int_var_casted_to_float)
print(type(float_var_casted_to_int))